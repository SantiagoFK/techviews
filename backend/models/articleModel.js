const mongoose = require('mongoose')

const Schema = mongoose.Schema

const articleSquema = new Schema({
    title: {
        type: String,
        required: true
    },
    author: {
        type: String,
        required: true
    },
    body: {
        type: String,
        required: true
    },
    created: {
        type: Date,
        default: Date.now
    },
    updated: {
        type: Date,
        default: Date.now
    }
})

module.exports = mongoose.model('Article', articleSquema)